class sa_layer(nn.Module):
    """Constructs a Channel Spatial Group module.
    Args:
        k_size: Adaptive selection of kernel size
    """

    def __init__(self, channel, groups=64):
        super(sa_layer, self).__init__()
        self.groups = groups
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.cweight = Parameter(torch.zeros(1, channel // (1 * groups), 1, 1))
        self.cbias = Parameter(torch.ones(1, channel // (1 * groups), 1, 1))
        self.sweight = Parameter(torch.zeros(1, channel // (1 * groups), 1, 1))
        self.sbias = Parameter(torch.ones(1, channel // (1 * groups), 1, 1))

        self.sigmoid = nn.Sigmoid()
        # self.gn = nn.GroupNorm(channel // (1 * groups), channel // (1 * groups))
        self.gn = nn.GroupNorm(8, 8)

    @staticmethod
    def channel_shuffle(x, groups):
        b, c, h, w = x.shape

        x = x.reshape(b, groups, -1, h, w)
        x = x.permute(0, 2, 1, 3, 4)

        # flatten
        x = x.reshape(b, -1, h, w)

        return x

    def forward(self, x): # 1, 512, 8, 8
        # print(x.shape)
        b, c, h, w = x.shape  # 1, 512, 8, 8

        x = x.reshape(b * 32, -1, h, w)
        # print(x)
        # print(x.shape)
        x_0, x_1 = x.chunk(2, dim=1)

        # channel attention
        xn = self.avg_pool(x_0)
        xn = self.cweight * xn + self.cbias
        xn = x_0 * self.sigmoid(xn)

        # spatial attention
        # print(x_1.shape)
        xs = self.gn(x_1)
        xs = self.sweight * xs + self.sbias
        xs = x_1 * self.sigmoid(xs)

        # concatenate along channel axis
        out = torch.cat([xn, xs], dim=1)
        out = out.reshape(b, -1, h, w)

        out = self.channel_shuffle(out, 2)
        return out


